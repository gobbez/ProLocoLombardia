    nome = ""
    global numero_trovate
    numero_trovate = 0

    url_base = "https://www.google.com"

    for nome in dizionario_città.values():
        print(f"Ricerco Pro Loco {nome} .... ", end=" ")
        # Imposta il percorso del tuo driver GeckoDriver
        driver_path = '/home/kali/geckodriver'

        # Configura le opzioni per la modalità testuale
        options = Options()
        options.headless = True

        # Inizializza il driver di Selenium per Firefox utilizzando un oggetto Service e le opzioni
